        <h2>Tema Administrativo</h2>
        <p>El derecho administrativo (del latín ad "junto a", y ministrare, "manejar las cosas comunes") es la rama del derecho público que regula la organización, funcionamiento, poderes y deberes de la Administración pública y las consiguientes relaciones jurídicas entre la Administración y otros sujetos.

        En otras palabras, el derecho administrativo es aquel que comprende la organización y el funcionamiento de toda forma de administración pública. Por extensión, suele ser también aplicable a la actuación materialmente administrativa de los demás poderes del Estado y de todos esos entes del sector público. Y todo ello, desde la doble perspectiva de procurar la eficacia de las Administraciones pero también garantizar los derechos de los particulares en sus relaciones con ellas.</p>
